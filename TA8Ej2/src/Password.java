
public class Password {

	int longitud=8;
	String pass;
	
	public Password() {
	}
	
	public Password(int longitud) {
		this.longitud = longitud;
	}

	public int getLongitud() {
		return longitud;
	}

	public String getPass() {
		return pass;
	}

	public void setLongitud(int longitud) {
		this.longitud = longitud;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}


	
	
}
